class Dog {

	private String name;
	private String breed;
	private double weight;

	public Dog(String name, String breed, double weight){
		this.name = name;
		this.breed = breed;
		this.weight = weight;
	}

	public String getName(){
		return this.name;
	}
	public String getBreed(){
		return this.breed;
	}

	public double getWeight(){
		return this.weight;
	}



}

	